public class Kosullar_8 {

	public static void main(String[] args) {
		
		int i = 5;
		
		if (i > 3) {
			System.out.println("i'nin değeri 3'den büyük");
		}
		
		if (i > 2) {
			System.out.println("i'nin değeri 2'den büyük");
		}
		
		else if (i > 1) {
			System.out.println("i'nin değeri 1'den büyük");
		}
		
		else {
			System.out.println("i'nin değeri 5 değildir");
		}
	}

}


